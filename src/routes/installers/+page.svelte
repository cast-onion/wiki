<h1>Installers</h1>

<p>Platform-specific installation methods.</p>

<h2>Ubuntu (Noble Numbat)</h2>
<ul>
  <li>Download the .deb and run this</li>
  <code>chmod +x ./cast-onion-*.deb && sudo apt install ./cast-onion-*.deb</code>
</ul>

<h2>Ubuntu (Jammy Jellyfish)</h2>
<ul>
  <li>Download the .deb and run this</li>
  <code>chmod +x ./cast-onion-*.deb && sudo apt install ./cast-onion-*.deb</code>
</ul>

<h2>Debian</h2>
<ul>
  <li>Download the .deb and run this</li>
  <code>chmod +x ./cast-onion-*.deb && sudo apt install ./cast-onion-*.deb</code>
</ul>

<h2>Flatpak</h2>
<ul>
  <li>Download the .flatpak and run this</li>
  <code>flatpak install /path/to/cast-onion-*.flatpak</code>
</ul>

<h2>Arch Linux</h2>
<ul>
  <li>You can install cast.onion client through an AUR helper like yay or Paru</li>
  <li>Get the latest stable version  from cast-onion-bin</li>
  <code>yay -S cast-onion-bin</code> or <code>paru -S cast-onion-bin</code>
</ul>

<h2>Flatpak</h2>
<ul>
  <li>Download the .flatpak and run this</li>
  <code>flatpak install /path/to/cast-onion-*.flatpak</code>
</ul>

<h2>Windows</h2>
<ul>
  <li>Download the .exe installer</li>
  <li>Double-click the file to run it</li>
  <li>Follow the installation prompts</li>
  <li>(Optional) If Windows shows a security warning, click “More info” → “Run anyway”</li>
</ul>

<h2>macOS</h2>
<ul>
  <li>Download the .dmg file and open it</li>
  <li>Drag the app into your Applications folder</li>
  <li>(Optional) If blocked, go to System Settings → Privacy & Security and click “Open Anyway”</li>
</ul>