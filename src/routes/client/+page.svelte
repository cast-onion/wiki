<h1>Client</h1>

<p>
The official client is used to transmit audio through the RTC WebSocket and to listen to broadcasts.
</p>

<h2>Features</h2>
<ul>
  <li>Audio is transmitted using the Opus codec at 48kHz stereo</li>
  <li>Host your own station using a Station Key and Access Token</li>
  <li>Private listening support</li>
</ul>

<h2>Logging</h2>
<p>
We log broadcasts and viewer counts, but we do not log IP addresses. This policy may change in the future, so please stay updated by reviewing the
<a href="https://castonion.xyz/tos">Terms of Service</a>.
</p>